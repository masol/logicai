// ===== 基础类型 =====

// 角色
export interface 角色 {
    id: string;
    名称: string;
    身份: '主角' | '反派' | '配角' | '对照角色';
    性格特征: string[];        // 如 ["勇敢", "多疑"]
    当前目标: string;
    情绪状态: 情绪;            // 当前情绪
}

// 情绪
export type 情绪 = '充满希望' | '恐惧' | '愤怒' | '悲伤' | '喜悦' | '平静';

// 情感弧线
export interface 情感弧线 {
    起始: 情绪;
    结束: 情绪;
    强度: number; // 1-5
}

// 场景类型
export type 场景类型 = '动作' | '对话' | '叙述' | '闪回' | '高潮' | '结局';
